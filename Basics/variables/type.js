//type of variables
let name = "Shivam";
console.log(typeof name); // Output: string
let age = 22;
console.log(typeof age); // Output: number
let isStudent = true;
console.log(typeof isStudent); // Output: boolean
let hobbies = ["coding", "gaming", "traveling"];
console.log(typeof hobbies); // Output: object
let person = { name: "Shivam", age: 22 };
console.log(typeof person); // Output: object
let undefinedVar;
console.log(typeof undefinedVar); // Output: undefined
let nullVar = null;
console.log(typeof nullVar); // Output: object (this is a quirk in JavaScript)
